public class StuffInCardTwo {
    private String name;
    private String amount;
    private String interest;

    public StuffInCardTwo(String name, String amount, String interest) {
        this.name = name;
        this.amount = amount;
        this.interest = interest;

    }

    public StuffInCardTwo() {
    }

    public String getName() {
        return name;
    }

    public String getAmount() {
        return amount;
    }

    public String getInterest() {
        return interest;
    }
}
